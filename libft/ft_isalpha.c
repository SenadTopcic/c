#include "libft.h"

int	ft_isalpha(int c)
{
	int	result;

	result = 1;
	if ((c >= 'A' && c <= 'Z' )
		|| (c >= 'a' && c <= 'z'))
	{
		result = 1;
	}
	else
		result = 0;
	return (result);
}
